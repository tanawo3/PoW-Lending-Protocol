ERROR_EXTERNAL = "[EXTERNAL]"    # Deterministic external oracle failure
ERROR_TRANSIENT = "[TRANSIENT]"  # Non-deterministic network partition
ERROR_LLM = "[LLM_ERROR]"        # Consensus breakdown or prompt hallucination

@allow_storage
@dataclass
class PoWSubmission:
    """
    Core data structure representing a Proof-of-Work backed loan.
    By utilizing strict dataclasses, we guarantee memory layout determinism
    across all nodes running GenVM, avoiding float/dict instantiation drifts.
    
    Attributes:
        proposal_id (str): UUID assigned by the external client.
        borrower (str): Sanitized identifier or wallet address of the borrower.
        requested_amount (u256): Requested capital in atto-scale precision.
        pow_submission (str): The borrower's github username or proof string.
        status (str): "PENDING" | "APPROVED" | "REJECTED" | "FLAGGED" | "REVOKED".
        ai_reasoning (str): Immutable summary generated by the AI leader.
        risk_score (u256): Dynamic risk assessment in basis points (0-10000).
        created_at (u256): Timestamp proxy (block epoch).
        last_updated (u256): Last state transition block.
    """
    proposal_id: str
    borrower: str
    requested_amount: u256
    pow_submission: str
    status: str
    ai_reasoning: str
    risk_score: u256
    collateral: u256
    debt: u256
    created_at: u256
    last_updated: u256

@allow_storage
@dataclass
class ProtocolState:
    """
    High-level encapsulation of the entire protocol's thermodynamic state.
    Used for mathematical divergence calculations and on-chain analytics.
    """
    total_processed: u256
    total_approved: u256
    total_rejected: u256
    total_revoked: u256
    total_capital_requested: u256
    total_capital_approved: u256
    global_risk_index_bps: u256

class PoWLendingProtocol(gl.Contract):
    """
    PoW Lending Protocol Enterprise Engine.
    
    This contract handles the ingestion, validation, and AI-driven underwriting
    of decentralized loans backed by developer reputation (Proof of Work). It guarantees 
    Byzantine fault tolerance by de-coupling deterministic state storage from non-deterministic LLM execution.
    """
    proposals: TreeMap[str, PoWSubmission]
    proposal_ids: DynArray[str]
    balances: TreeMap[str, u256]
    state: ProtocolState
    owner: str

    def __init__(self):
        """
        Initializes the state trees. TreeMaps provide O(log n) deterministic
        lookups, satisfying strict GenLayer performance constraints.
        """
        self.owner = str(gl.message.sender_address)
        self.state.total_processed = u256(0)
        self.state.total_approved = u256(0)
        self.state.total_rejected = u256(0)
        self.state.total_revoked = u256(0)
        self.state.total_capital_requested = u256(0)
        self.state.total_capital_approved = u256(0)
        self.state.global_risk_index_bps = u256(0)

    # -------------------------------------------------------------------------
