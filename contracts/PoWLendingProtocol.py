    @gl.public.write.payable
    def submit_proposal(self, proposal_id: str, borrower: str, requested_amount: int, pow_submission: str) -> bool:
        """
        Ingests a new loan proposal from an external client.
        
        Args:
            proposal_id: External UUID string.
            borrower: Identifier string.
            requested_amount: Integer representing capital.
            pow_submission: Unstructured text detailing the proof of work or GitHub handle.
            
        Returns:
            bool: True if ingestion was successful.
            
        Raises:
            gl.vm.UserError: If inputs violate deterministic invariants.
        """
        if proposal_id in self.proposals:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Proposal already exists")
            
        if requested_amount <= 0:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Amount must be > 0")
        
        # Rigorous input sanitization against prompt injection attacks
        clean_borrower = _deep_sanitize(borrower)[:128]
        clean_pow = _deep_sanitize(pow_submission)[:MAX_POW_LEN]
        
        prop = PoWSubmission(
            proposal_id=proposal_id,
            borrower=str(gl.message.sender_address),
            requested_amount=u256(requested_amount),
            pow_submission=clean_pow,
            status="PENDING",
            ai_reasoning="",
            risk_score=u256(0),
            collateral=u256(gl.message.value),
            debt=u256(0),
            created_at=u256(0), # Epoch 0
            last_updated=u256(0)
        )
        self.proposals[proposal_id] = prop
        self.proposal_ids.append(proposal_id)
        
        self.state.total_capital_requested = u256(int(self.state.total_capital_requested) + requested_amount)
        return True

    @gl.public.write
    def evaluate_proposal(self, proposal_id: str) -> bool:
        """
        Triggers the GenLayer Subjective Consensus engine to underwrite the loan.
        
        This method utilizes a Leader-Validator multi-agent protocol. 
        The Leader executes a complex LLM prompt to determine risk, pulling live GitHub data.
        The Validators independently critique the Leader's reasoning and ensure
        it satisfies the required logical constraints before persisting to state.
        
        Args:
            proposal_id (str): Target proposal ID to underwrite.
            
        Returns:
            bool: True if consensus was reached and state was updated.
        """
        if str(gl.message.sender_address) != self.owner:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Unauthorized: Only the owner can evaluate proposals")
            
        if proposal_id not in self.proposals:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Proposal not found")
            
        prop = self.proposals[proposal_id]
        if prop.status != "PENDING":
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Not pending")
            
        # Extract to local variables to prevent state access inside nondet block
        borrower = prop.borrower
        amount = int(prop.requested_amount)
        pow_sub = prop.pow_submission
        
        def leader_fn() -> dict:
            """Leader execution environment."""
            github_data = _fetch_github(pow_sub)
            prompt = _interpret_leader_prompt(borrower, amount, pow_sub, github_data)
            analysis = gl.nondet.exec_prompt(prompt, response_format="json")
            return {
                "verdict": _parse_verdict(analysis),
                "risk_score": _parse_ratio_bps(analysis),
                "summary": _clean_summary(analysis)
            }
            
        def validator_fn(leader_res: gl.vm.Result) -> bool:
            if not isinstance(leader_res, gl.vm.Return):
                return _handle_leader_error(leader_res, leader_fn)
                
            try:
                mine = leader_fn()
                ld_data = leader_res.calldata
                return mine["verdict"] == ld_data["verdict"] and isinstance(ld_data.get("risk_score"), int)
            except gl.vm.UserError:
                return False

        # Execute Consensus Engine deterministically
        decision = gl.vm.run_nondet(leader_fn, validator_fn)
        
        # State transitions based on consensus outcome
        prop.status = decision["verdict"]
        prop.ai_reasoning = decision["summary"]
        prop.risk_score = u256(decision["risk_score"])
        prop.last_updated = u256(int(prop.last_updated) + 1)
        if prop.status == "APPROVED":
            loan_amount = int(prop.requested_amount)
            prop.debt = u256(loan_amount + loan_amount // 20)  # 5% interest
            
            # Map requested principal to a defined lender-funded disbursement
            current_balance = int(self.balances.get(prop.borrower, u256(0)))
            self.balances[prop.borrower] = u256(current_balance + loan_amount)
            
            # Actual GenLayer native token disbursement
            if loan_amount > 0:
                _NativeRecipient(Address(prop.borrower)).emit_transfer(value=u256(loan_amount))
            
        self.proposals[proposal_id] = prop
        
        # Update System Metrics
        self.state.total_processed = u256(int(self.state.total_processed) + 1)
        if prop.status == "APPROVED":
            self.state.total_approved = u256(int(self.state.total_approved) + 1)
            self.state.total_capital_approved = u256(int(self.state.total_capital_approved) + amount)
        else:
            self.state.total_rejected = u256(int(self.state.total_rejected) + 1)
            
        # Recalculate global risk index
        self._recalculate_global_risk()
        return True


    @gl.public.write.payable
    def repay_loan(self, proposal_id: str) -> bool:
        """
        Allows a borrower to repay their debt and reclaim their collateral.
        Enforces strict authorization and value checking.
        """
        if proposal_id not in self.proposals:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Proposal not found")
        prop = self.proposals[proposal_id]
        if str(gl.message.sender_address) != prop.borrower:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Unauthorized: Not the borrower")
        if prop.status != "APPROVED":
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Loan is not in active state")
        debt = int(prop.debt)
        if gl.message.value < debt:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Insufficient repayment. Need {debt}")
            
        # Refund collateral securely
        collateral_amount = int(prop.collateral)
        if collateral_amount > 0:
            _NativeRecipient(Address(prop.borrower)).emit_transfer(value=u256(collateral_amount))
            
        prop.status = "REPAID"
        prop.debt = u256(0)
        prop.last_updated = u256(int(prop.last_updated) + 1)
        self.proposals[proposal_id] = prop
        return True

    @gl.public.write
    def revoke_proposal(self, proposal_id: str) -> bool:
        """
        Allows an external system or governance module to forcibly revoke 
        a proposal that was maliciously submitted or violated TOS.
        """
        if proposal_id not in self.proposals:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Proposal not found")
            
        prop = self.proposals[proposal_id]
        if str(gl.message.sender_address) != prop.borrower:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Unauthorized: Not the borrower")
        if prop.status == "REVOKED":
            return True # Idempotent
        if prop.status == "PENDING":
            collateral_amount = int(prop.collateral)
            if collateral_amount > 0:
                _NativeRecipient(Address(prop.borrower)).emit_transfer(value=u256(collateral_amount))
                
        old_status = prop.status
        prop.status = "REVOKED"
        prop.ai_reasoning = "Revoked by borrower."
        prop.last_updated = u256(int(prop.last_updated) + 1)
        self.proposals[proposal_id] = prop
        
        self.state.total_revoked = u256(int(self.state.total_revoked) + 1)
        
        if old_status == "APPROVED":
            self.state.total_approved = u256(int(self.state.total_approved) - 1)
            self.state.total_capital_approved = u256(int(self.state.total_capital_approved) - int(prop.requested_amount))
            self._recalculate_global_risk()
            
        return True

