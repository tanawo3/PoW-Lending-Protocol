    @gl.public.view
    def simulate_loan_default_probability(self, proposal_id: str) -> str:
        """
        Deterministic mathematical model to project default probability 
        based strictly on the AI-assigned risk score and system metrics.
        """
        if proposal_id not in self.proposals:
            return "0.0"
        p = self.proposals[proposal_id]
        if p.status != "APPROVED":
            return "0.0"
            
        risk = int(p.risk_score)
        global_risk = int(self.state.global_risk_index_bps)
        
        # If the loan is riskier than the global average, penalize heavily.
        if risk > global_risk and global_risk > 0:
            multiplier = (risk * 1000) // global_risk
            projected = (risk * multiplier) // 1000
        else:
            projected = risk
            
        projected = min(projected, BPS_DENOMINATOR)
        return str(projected / 100.0)

